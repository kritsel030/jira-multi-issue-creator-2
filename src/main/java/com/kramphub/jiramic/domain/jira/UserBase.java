package com.kramphub.jiramic.domain.jira;

// Used to represent the assignee or reporter for a new issue
public class UserBase {
    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
